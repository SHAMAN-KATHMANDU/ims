(() => {
  const bits = new Set<number>();
  const keys = new Set<string>();
  for (const perm of PERMISSIONS) {
    if (bits.has(perm.bit)) {
      throw new Error(`Duplicate bit ${perm.bit}: ${perm.key}`);
    }
    if (keys.has(perm.key)) {
      throw new Error(`Duplicate key: ${perm.key}`);
    }
    bits.add(perm.bit);
    keys.add(perm.key);
  }
})();

