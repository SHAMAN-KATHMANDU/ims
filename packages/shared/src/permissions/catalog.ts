/**
 * Permission Catalog — Single source of truth for all system permissions.
 * ~365 permissions across 6 modules, 512-bit bitset (64 bytes).
 * Append-only: bits are never reused. ADMINISTRATOR is pinned at bit 511.
 */

export interface PermissionDef {
  key: string;
  bit: number;
  module: ModuleId;
  submodule: string;
  action: string;
  label: string;
  description: string;
  dangerous?: boolean;
  implies?: string[];
}

export type ModuleId =
  | "INVENTORY"
  | "SALES"
  | "CRM"
  | "WEBSITE"
  | "REPORTS"
  | "SETTINGS";

